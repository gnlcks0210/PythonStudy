# while문
#  - 조건이 참인 동안 지속적으로 코드를 반복 실행하는 문법
#  - 조건이 언젠가는 False가 나오도록 해주어야 함
#     > False가 안나오면 무한히 코드가 반복되는 무한 루프가 발생

count = 10

while count > 7:
    print(count)
    count -= 1

# 사용자로부터 돈(money)을 입력받아 아이스크림을 구매하는 코드
#  - 아이스크림은 1,000원
money = int(input("얼마있으신가요? : "))
count = 1

while money >= 1000:
    print("아이스크림을 구매했습니다.")

    money -= 1000
    print(f"남은 금액은 {money}원 입니다.")
    print(f"{count}번째 아이스크림을 구매했습니다.")

    count += 1

