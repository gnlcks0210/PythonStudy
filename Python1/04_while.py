# while문의 특징 : 몇번 반복해야할지 순서를 모를 때 사용

# 카페 주문을 받는 프로그램
#  - 처음에 메뉴를 보여줌
#  - 사용자로부터 메뉴를 입력받음
#  - 1번을 누르면 "아이스 아메리카노를 주문하셨습니다." 출력
#  - 2번을 누르면 "카페라떼를 주문하셨습니다." 출력
#  - 3번을 누르면 "딸기 요거트 스무디를 주문하셨습니다." 출력
#  - 0번을 누르면 "프로그램이 종료됩니다." 출력 후 while문 종료
#  - 그 외의 번호를 누르면 "잘못된 메뉴를 선택하셨습니다." 출력
is_on = True

while is_on:
    print("-----카페 주문 프로그램-----")
    print("1. 아이스 아메리카노")
    print("2. 카페라떼")
    print("3. 딸기 요거트 스무디")
    print("0. 프로그램 종료")
    print("-------------------------")
    menu = int(input("메뉴 선택 : "))

    # if문 사용해서 밑에 코드를 완성
    if menu == 1:
        print("아이스 아메리카노를 주문하셨습니다.")
    elif menu == 2:
        print("카페라떼를 주문하셨습니다.")
    elif menu == 3:
        print("딸기 요거트 스무디를 주문하셨습니다.")
    elif menu == 0:
        print("프로그램이 종료되었습니다.")
        is_on = False
    else:
        print("잘못된 메뉴를 선택하셨습니다.")



